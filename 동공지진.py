import turtle as t



t.shape("circle")
t.shapesize(2,2,2)
t.speed(0)
t.penup()
ada = t.Turtle()
ada.penup()
ada.speed(0)
ada.shape("circle")
ada.shapesize(2,2,2)

ada.write("(;;O_O)", font =("Arial",100,"normal"))

for i in range(1000):
    t.goto(163,75)
    ada.goto(352,75)
    t.goto(172,75)
    ada.goto(342,75)
    
    
